package com.human.ex;

public class JavaTest04 {

	public static void main(String[] args) {
		//문제 1
		String a1 = "홍길동";
		int a2 = 29;
		int a3 = 170;
		
		System.out.println("이름:"+ a1);
		System.out.println("나이:"+ a2);
		System.out.println("키:" + a3);
		System.out.println("\n");
		
		//문제 2
		System.out.println("정수자료형 : int , byte , short , long");
		System.out.println("실수자료형 : float , double");
		System.out.println("문자자료형 : char");
		System.out.println("논리형 : boolean");
		System.out.println("참조자료형 : 문자열 String , 배열 , 클래스 , 열거형");
		System.out.println("\n");
		
		//문제 3
		System.out.println("10 : int 형");
		System.out.println("1. : double 형");
		System.out.println("6F : float 형");
		System.out.println("23d : double 형");
		System.out.println("14E10 : double 형");
		System.out.println("\'a\' : char 형");
		System.out.println("\n");
		
		//문제 4
		int a = 5;
		int b = 10;
		
		System.out.println( a + b );
		System.out.println("\n");
		
		

	}

}
